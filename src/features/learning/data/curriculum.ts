// ===== EXPORT =====

export const curriculum: CurriculumWorld[] = [world1, world2, world3, world4, world5, world6];

/** Get a world by ID */
export function getWorld(worldId: string): CurriculumWorld | undefined {
  return curriculum.find((w) => w.id === worldId);
}

/** Get all lessons for a world */
export function getWorldLessons(worldId: string): CurriculumLesson[] {
  const world = getWorld(worldId);
  if (!world) return [];
  return world.units.flatMap((u) => u.lessons);
}

/** Get a specific lesson */
export function getLesson(lessonId: string): CurriculumLesson | undefined {
  for (const world of curriculum) {
    for (const unit of world.units) {
      const lesson = unit.lessons.find((l) => l.id === lessonId);
      if (lesson) return lesson;
    }
  }
  return undefined;
}

/** Total vocabulary words across curriculum */
export function getTotalVocabularyCount(): number {
  const words = new Set<string>();
  for (const world of curriculum) {
    for (const unit of world.units) {
      for (const lesson of unit.lessons) {
        for (const word of lesson.vocabulary) {
          words.add(word.toLowerCase());
        }
      }
    }
  }
  return words.size;
}

/** Summary stats */
export function getCurriculumStats() {
  let totalLessons = 0;
  let totalUnits = 0;
  let bossLessons = 0;
  for (const world of curriculum) {
    totalUnits += world.units.length;
    for (const unit of world.units) {
      totalLessons += unit.lessons.length;
      bossLessons += unit.lessons.filter((l) => l.type === 'boss').length;
    }
  }
  return {
    worlds: curriculum.length,
    units: totalUnits,
    lessons: totalLessons,
    bossLessons,
    vocabularyWords: getTotalVocabularyCount(),
  };
}
