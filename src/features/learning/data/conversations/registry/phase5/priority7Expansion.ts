import type {
  ConversationGoalType,
  ConversationNodeV2,
  ConversationScenario,
} from '../../types/conversationScenario';

interface ExpansionScenarioSpec {
  idBase: string;
  title: string;
  titleTr: string;
  summary: string;
  summaryTr: string;
  theme: 'art' | 'nature' | 'time';
  subTheme: string;
  sceneEmoji: string;
  targetWords: string[];
  targetPatterns: [string, string, string];
  learningGoals: string[];
  promptWords: [string, string, string, string, string, string];
  promptIcons: [string, string, string, string, string, string];
  stepTexts: [string, string, string, string, string, string];
  stepTextsTr: [string, string, string, string, string, string];
  acceptedPhrases: [string, string, string, string, string, string];
  acceptedPhrasesTr: [string, string, string, string, string, string];
  rewardId: string;
  worldTag: 'w9' | 'w11' | 'w12';
}

function sentenceToVariants(sentence: string, extra: string[] = []): string[] {
  const base = sentence.toLowerCase().replace(/[!?.,]/g, '');
  return [...new Set([base, ...extra.map((item) => item.toLowerCase())])];
}

function createPromptNode(
  index: number,
  _spec: ExpansionScenarioSpec,
  text: string,
  textTr: string,
  expectedText: string,
  expectedTextTr: string,
  targetWord: string,
  emoji: string,
  nextNodeId: string,
  goalType: ConversationGoalType,
  pattern?: string,
): ConversationNodeV2 {
  return {
    id: `n${index}`,
    speaker: 'nova',
    role: 'friend',
    text,
    textTr,
    emoji,
    goalType,
    targetWord,
    targetPattern: pattern,
    hint: {
      delayMs: 7000,
      text: `Say: ${expectedText}`,
      textTr: `Söyle: ${expectedText}`,
      revealPattern: Boolean(pattern),
    },
    repair: {
      enabled: true,
      prompt: `Try: ${expectedText}`,
      promptTr: `Dene: ${expectedText}`,
      maxRetries: 2,
    },
    responses: [
      {
        id: `r${index}a`,
        expectedText,
        expectedTextTr,
        acceptedVariants: sentenceToVariants(expectedText, [targetWord]),
        acceptedWords: [targetWord],
        nextNodeId,
        emoji,
        marksTargetWord: [targetWord],
        marksPattern: pattern ? [pattern] : undefined,
      },
    ],
  };
}

